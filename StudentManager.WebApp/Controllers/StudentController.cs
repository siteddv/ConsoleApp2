using Microsoft.AspNetCore.Mvc;
using StudentManager.WebApp.ViewModels;

namespace StudentManager.WebApp.Controllers
{
    public class StudentController : Microsoft.AspNetCore.Mvc.Controller
    {
        // GET: StudentController
        public ActionResult Index()
        {
            Controller.Controllers.StudentController studentController 
                = new Controller.Controllers.StudentController();

            var students = studentController.GetAllStudents();

            var studentViewModels = students.Select(st 
                => new StudentViewModel()
            {
                Name = st.Name,
                Age = st.Age
            });

            return View(studentViewModels);
        }

        // GET: StudentController/Details/5
        public ActionResult Details(int id)
        {
            return View();
        }

        // GET: StudentController/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: StudentController/Create
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create(IFormCollection collection)
        {
            try
            {
                return RedirectToAction(nameof(Index));
            }
            catch
            {
                return View();
            }
        }

        // GET: StudentController/Edit/5
        public ActionResult Edit(int id)
        {
            return View();
        }

        // POST: StudentController/Edit/5
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit(int id, IFormCollection collection)
        {
            try
            {
                return RedirectToAction(nameof(Index));
            }
            catch
            {
                return View();
            }
        }

        // GET: StudentController/Delete/5
        public ActionResult Delete(int id)
        {
            return View();
        }

        // POST: StudentController/Delete/5
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Delete(int id, IFormCollection collection)
        {
            try
            {
                return RedirectToAction(nameof(Index));
            }
            catch
            {
                return View();
            }
        }
    }
}
